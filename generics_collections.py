from typing import List, Dict, Tuple, Set

# List of integers
numbers: List[int] = [1, 2, 3, 4, "pepito"]

# Dictionary mapping strings to floats
price_map: Dict[str, float] = {"apple": 0.99, "banana": 0.59}

# Tuple containing a string and an integer
person: Tuple[str, int] = ("Alice", 30)

# Set of unique strings
unique_names: Set[str] = {"Alice", "Bob", "Charlie"}

print(numbers, price_map, person, unique_names)
